package paquete1;

public class Persona {
    
    //Atributos
    protected String nombre;

    //Constructor
    public Persona(String nombre) {
        this.nombre = nombre;
    }
    
    //Mutadores
    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
    
    
    
}
